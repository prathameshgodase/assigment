package Java8;

public class Employee {
	int id;
	String name;
	
public Employee(int id,String name) {
	super();
	this.id=id;
	this.name=name;
	}
public int getid() {
	return id;
}
public void setid() {
	this.id=id;
	
}
public String getname() {
	return name;
}
public void setName() {
	this.name=name;
}
}
